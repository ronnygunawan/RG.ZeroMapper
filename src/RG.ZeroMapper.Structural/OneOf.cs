namespace RG.ZeroMapper.Structural;

/// <summary>
/// Base class for discriminated union types. A OneOf type can hold a value of any of the generic type arguments.
/// </summary>
public abstract class OneOf<T1>
{
}

/// <summary>
/// Base class for discriminated union types. A OneOf type can hold a value of any of the generic type arguments.
/// </summary>
public abstract class OneOf<T1, T2>
{
}

/// <summary>
/// Base class for discriminated union types. A OneOf type can hold a value of any of the generic type arguments.
/// </summary>
public abstract class OneOf<T1, T2, T3>
{
}

/// <summary>
/// Base class for discriminated union types. A OneOf type can hold a value of any of the generic type arguments.
/// </summary>
public abstract class OneOf<T1, T2, T3, T4>
{
}
