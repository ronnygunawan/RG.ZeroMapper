namespace RG.ZeroMapper.Structural;

/// <summary>
/// Base class for union types. A union type contains all properties from all generic type arguments.
/// </summary>
public abstract class Union<T1>
{
}

/// <summary>
/// Base class for union types. A union type contains all properties from all generic type arguments.
/// </summary>
public abstract class Union<T1, T2>
{
}

/// <summary>
/// Base class for union types. A union type contains all properties from all generic type arguments.
/// </summary>
public abstract class Union<T1, T2, T3>
{
}

/// <summary>
/// Base class for union types. A union type contains all properties from all generic type arguments.
/// </summary>
public abstract class Union<T1, T2, T3, T4>
{
}
